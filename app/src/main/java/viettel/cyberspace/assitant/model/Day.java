public class Day {
    int time;
    int reviewed;
    int allRecordered;
    int timeReviewed;
    int timeRecorder;

    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }

    public int getReviewed() {
        return reviewed;
    }

    public void setReviewed(int reviewed) {
        this.reviewed = reviewed;
    }

    public int getAllRecordered() {
        return allRecordered;
    }

    public void setAllRecordered(int allRecordered) {
        this.allRecordered = allRecordered;
    }

    public int getTimeReviewed() {
        return timeReviewed;
    }

    public void setTimeReviewed(int timeReviewed) {
        this.timeReviewed = timeReviewed;
    }

    public int getTimeRecorder() {
        return timeRecorder;
    }

    public void setTimeRecorder(int timeRecorder) {
        this.timeRecorder = timeRecorder;
    }
}
